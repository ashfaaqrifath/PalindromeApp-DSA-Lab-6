    public void shuffleArray() {

        int index[] = new int[4];
        ArrayList<Integer> list = new ArrayList<Integer>();
        for (int i = 0; i < 30; i++) {
            list.add(new Integer(i));
        }
        Collections.shuffle(list);
        for (int i = 0; i < 4; i++) {
            index[i] = list.get(i);
        }

        cardLables[0] = normalWords[index[0]];
        cardLables[1] = normalWords[index[1]];
        cardLables[2] = normalWords[index[2]];
        cardLables[3] = palindromeWords[index[3]];

        list.clear();
        for (int i = 0; i < 4; i++) {
            list.add(new Integer(i));
        }
        Collections.shuffle(list);

        int rgen = list.get(0);

        for (int i = 0; i < 4; i++) {
            int randomPosition = rgen;
            String temp = cardLables[i];
            cardLables[i] = cardLables[randomPosition];
            cardLables[randomPosition] = temp;
        }

    }

