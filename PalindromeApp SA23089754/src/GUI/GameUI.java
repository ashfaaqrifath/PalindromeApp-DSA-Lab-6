    public boolean palindromeChecker(String input) {
        //complete the code
        

        String temp2 = input.replaceAll("[^a-zA-Z0-9]", "").toLowerCase();
        Stack stack = new Stack();

        for (char c : temp2.toCharArray()) {
            stack.push(c);
        }
        
        for (char c : temp2.toCharArray()) {
            Link link = stack.pop();
            
            if (link == null || link.letter != c) {
                return false;
            }
        }

        return true;
        
    }

